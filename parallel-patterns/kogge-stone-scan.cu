int main() {
    int N = 1026;  

    float* output = (float*) malloc(N*sizeof(float));

    std::vector<float> input(N);
    std::vector<float> cpu_output(N);
    std::vector<float> gpu_output(N);

    // initialize input
    for (int i = 0; i < N; i++) {
        input[i] = i + 1;   // [1,2,3,...]
    }

    // CPU reference
    cpu_scan(input, cpu_output);

    // GPU call
    gpu_scan(input.data(), output, N);
    std::copy(output, output + N, gpu_output.begin());

    // print results
    std::cout << "Input:       ";
    for (auto v : input) std::cout << v << " ";
    std::cout << "\n";

    std::cout << "CPU Scan:    ";
    for (auto v : cpu_output) std::cout << v << " ";
    std::cout << "\n";

    std::cout << "GPU Scan:    ";
    for (auto v : gpu_output) std::cout << v << " ";
    std::cout << "\n";

    // validation
    bool correct = true;
    for (int i = 0; i < N; i++) {
        if (fabs(cpu_output[i] - gpu_output[i]) > 1e-5) {
            correct = false;
            std::cout << "Mismatch at " << i 
                      << " CPU=" << cpu_output[i] 
                      << " GPU=" << gpu_output[i] << "\n";
        }
    }

    if (correct) {
        std::cout << "✅ Scan is CORRECT\n";
    } else {
